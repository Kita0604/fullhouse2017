var windowStatePlugin = {
  install: function(Vue) {
    // ウィンドウの状態
    var state = {
      scrollX: 0,
      scrollY: 0,
      width: 0,
      height: 0
    }
    // スクロール数値を取得
    var onScroll = function() {
      state.scrollX = window.pageXOffset
      state.scrollY = window.pageYOffset
    }
    document.addEventListener('scroll', onScroll)
    // ウィンドウのサイズを取得
    var onResize = function() {
      state.width = document.documentElement.clientWidth
      state.height = document.documentElement.clientHeight
    }
    window.addEventListener('resize', onResize)
    // 初期値を更新
    window.addEventListener('load', function() {
      onScroll()
      onResize()
    })
    // プロパティ $window を定義
    Vue.util.defineReactive(Vue.prototype, '$window', state)
  }
}

Vue.use(windowStatePlugin)
new Vue({
  el: '#time_schedule',
  data: {
    band_list: [
      {band_day: band_day1},
      {band_day: band_day2},
      {band_day: band_day3},
    ],
    showed_band: 0,
    show: true,
    is_tab_follow: false,
    posY: 0,
  },


  methods: {
    getPosition: function() {
      // schedule_top_inner - header で active
      this.posY = document.getElementById("schedule_top_inner").getBoundingClientRect().top + window.pageYOffset - 60
    },
  },

  watch: {
    '$window.height': 'getPosition'
  },

  mounted: function() {
    this.getPosition()
    var unwatch = this.$watch('$window.scrollY', function(scroll) {
      this.$nextTick(function() {
        //console.log(window.pageYOffset );

        if (this.posY < scroll) {
          this.is_tab_follow = true
          //unwatch()
        }
        else{
          this.is_tab_follow = false
        }
      })
    }, { immediate: true })
  }

});
